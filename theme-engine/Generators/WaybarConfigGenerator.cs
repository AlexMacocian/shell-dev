                "format-charging": "\uf0e7 {capacity}% <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "format-plugged": "\uf1e6 {capacity}% <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "format-alt": "\uf240 {time} <span color='{{c.Accent1}}'>{{w.Separator}}</span>"
              },

              "power-profiles-daemon": {
                "format": "\uf0e7 <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "tooltip-format": "Power profile: {profile}\nDriver: {driver}",
                "tooltip": true
              },

              "network": {
                "format-wifi": "\uf1eb {essid} <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
