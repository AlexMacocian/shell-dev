using System.Globalization;
using System.Text.Json;

namespace ThemeEngine.Generators;

public class WaybarConfigGenerator : IGenerator
{
  public string Name => "Waybar Config";
  public string OutputPath => ".config/waybar/config.jsonc";

  public string Generate(Theme theme, string wallpapersDir)
  {
    var w = theme.Waybar;
    var c = theme.Colors;
    var labels = w.WorkspaceLabels;

    // Build format-icons object
    var iconEntries = new List<string>();
    for (var i = 0; i < labels.Length; i++)
      iconEntries.Add($"      \"{i + 1}\": \"{labels[i]}\"");
    var formatIcons = string.Join(",\n", iconEntries);

    return $$"""
            // Auto-generated by ThemeEngine — do not edit manually
            // Theme: {{theme.Name}}
            {
              "layer": "top",
              "height": {{w.Height}},
              "spacing": 0,

              "hyprland/workspaces": {
                "format": "{icon}",
                "on-click": "activate",
                "format-icons": {
            {{formatIcons}}
                }
              },

              "hyprland/window": {
                "max-length": 40,
                "separate-outputs": true
              },

              "modules-left": [
                "hyprland/workspaces",
                "hyprland/submap"
              ],

              "modules-center": [
                "clock"
              ],

              "modules-right": [
                "custom/recording",
                "pulseaudio",
                "network",
                "custom/bluetooth",
                "cpu",
                "memory",
                "custom/gpu",
                "disk",
                "temperature",
                "backlight",
                "battery",
                "power-profiles-daemon",
                "tray",
                "custom/updates",
                "custom/power"
              ],

              "custom/bluetooth": {
                "exec": "~/.config/hypr/scripts/bluetooth-status.sh",
                "return-type": "json",
                "interval": 5,
                "on-click": "kitty --title bluetuith --override background_opacity=0.95 -e bluetuith",
                "tooltip": true
              },

              "custom/updates": {
                "exec": "~/.config/hypr/scripts/update-check.sh",
                "return-type": "json",
                "interval": 600,
                "signal": 8,
                "on-click": "kitty --title updates --override background_opacity=0.95 -e bash -c 'if command -v paru &>/dev/null; then paru -Syu; elif command -v yay &>/dev/null; then yay -Syu; else sudo pacman -Syu; fi; echo; echo Done. Press enter to close.; read; pkill -RTMIN+8 waybar'",
                "tooltip": true
              },

              "clock": {
                "format": "\uf017 {:%H:%M  {{w.Separator}}  %a, %b %d}",
                "tooltip-format": "<big>{:%Y %B}</big>\n<tt><small>{calendar}</small></tt>",
                "format-alt": "\uf017 {:%Y-%m-%d  %H:%M:%S}"
              },

              "cpu": {
                "format": "\uf2db {usage}% <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "tooltip": true,
                "interval": 2
              },

              "memory": {
                "format": "\uf233 {}% <span color='{{c.Accent1}}'>{{w.Separator}}</span>"
              },

              "custom/gpu": {
                "exec": "~/.config/hypr/scripts/gpu-usage.sh",
                "return-type": "json",
                "interval": 5,
                "tooltip": true
              },

              "disk": {
                "format": "\uf0a0 {percentage_used}% <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "tooltip-format": "{used} / {total} on {path}",
                "path": "/",
                "interval": 30
              },

              "temperature": {
                "critical-threshold": 80,
                "format": "\uf2c9 {temperatureC}\u00b0C <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "format-critical": "\uf2c7 {temperatureC}\u00b0C <span color='{{c.Accent1}}'>{{w.Separator}}</span>"
              },

              "backlight": {
                "format": "\uf185 {percent}% <span color='{{c.Accent1}}'>{{w.Separator}}</span>"
              },

              "battery": {
                "states": {
                  "warning": 30,
