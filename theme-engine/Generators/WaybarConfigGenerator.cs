                "scroll-step": 2,
                "format-bluetooth": "\uf294 {volume}% <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "format-bluetooth-muted": "\uf294 muted <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "format-muted": "\uf026 muted <span color='{{c.Accent1}}'>{{w.Separator}}</span>",
                "on-click": "kitty --title pulsemixer --override background_opacity=0.95 -e pulsemixer"
              },

              "tray": {
                "icon-size": 16,
                "spacing": 8
              },

              "custom/recording": {
                "exec": "~/.config/hypr/scripts/recording-status.sh",
                "return-type": "json",
                "interval": "once",
                "signal": 9,
                "tooltip": true
              },

              "custom/power": {
                "format": "\uf011",
                "tooltip": false,
                "on-click": "~/.config/hypr/scripts/power-menu.sh"
              }
            }
            """;
  }
}
